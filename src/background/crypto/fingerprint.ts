/**
 * Compute the raw 3-byte fingerprint of a master password.
 *
 * The returned bytes can be turned into an emoji string with `formatFingerprint`.
 */
export async function fingerprintMaster(master: string): Promise<Uint8Array> {
  const hex = await argon2id({
    password: master,
    salt: FINGERPRINT_SALT,
    parallelism: FINGERPRINT_PARAMS.parallelism,
    iterations: FINGERPRINT_PARAMS.iterations,
    memorySize: FINGERPRINT_PARAMS.memorySize,
    hashLength: FINGERPRINT_PARAMS.hashLength,
    outputType: "hex",
  });
  return new Uint8Array([
    Number.parseInt(hex.slice(0, 2), 16),
    Number.parseInt(hex.slice(2, 4), 16),
    Number.parseInt(hex.slice(4, 6), 16),
  ]);
}

/** Format a 3-byte fingerprint as a space-joined emoji triplet. */
export function formatFingerprint(bytes: Uint8Array): string {
  if (bytes.length < 3) {
    throw new RangeError(`fingerprint must be at least 3 bytes, got ${bytes.length}`);
  }
  return [0, 1, 2].map((i) => FINGERPRINT_EMOJIS[bytes[i]!]!).join(" ");
}
